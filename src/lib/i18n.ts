export const TRANSLATIONS: Record<Lang, Dict> = { es: ES, en: EN };

/** Devuelve la traducción de la clave; cae a ES, luego a la clave. */
export function t(key: string, lang: Lang = "es", vars?: Record<string, string | number>): string {
  const dict = TRANSLATIONS[lang] ?? ES;
  let str = dict[key] ?? ES[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replaceAll(`{${k}}`, String(v));
    }
  }
  return str;
}
